<p>Smoked Ribs With Red Pepper Jelly</p>

<p>Ingredients: 1-­‐3 Rack of Ribs, Meat Tenderizer, Rub of choice, 1-­‐2 Bottles Red Pepper Jelly, 1-­‐2 Bottle BBQ sauce</p>
<p>Tools: Grill/Smoker, Pizza Stone, Rib Rack, Spreader</p>

<p>Total prep time-­‐ 5-­‐10 minutes Total cook time-­‐ 3 hours</p>

<p>Grab a log of your choice to smoke the ribs, and cut into 1-­‐2 chunks to place in the smoker. Set a cooking stone down on the grill. This deflects heat, and allows the ribs to stay tender and keeps from burning.</p>

<img alt="image" src="images/recipes/ribs/Image_002.jpg" width="571">

<img alt="image" src="images/recipes/ribs/Image_003.jpg" width="530">

<p>Place your rack of ribs on a pan, and sprinkle with meat tenderizer, and a rub of your choice. For this I used a rub I like called Butt Rub.</p>

<img alt="image" src="images/recipes/ribs/Image_004.jpg" width="530">

<p>Rub your seasoning on by hand, fully covering the rack of ribs.</p>

<img alt="image" src="images/recipes/ribs/Image_005.jpg" width="582">
<p>Place your ribs in your smoker, and let sit for 2.5 hours.</p>
<img alt="image" src="images/recipes/ribs/Image_006.jpg" width="571">
<p>Take your ribs off of your pizza stone. Grab your Red Pepper Jelly and dollop a spoon full or two onto your rack of ribs. Let the Jelly melt for around 5 minutes.</p>

<img alt="image" src="images/recipes/ribs/Image_007.jpg" width="571">
<p>With a brush, or other spreader, spread the red pepper jelly over the top of the ribs and let sit for around 15-­‐20 minutes.</p>

<img alt="image" src="images/recipes/ribs/Image_008.jpg" width="571">
<p>Spread your BBQ sauce over the top of the jelly covered ribs, and let sit for around 4-­‐5 minutes.</p>

<img alt="image" src="images/recipes/ribs/Image_009.jpg" width="571">
<p>Pull your ribs off the grill onto a pan, and prepare to serve.</p>

<img alt="image" src="images/recipes/ribs/Image_010.jpg" width="571">
