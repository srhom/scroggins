		<h2>Smoked Ribs With Red Pepper Jelly</h2>

<p>I love to cook ribs! It’s one of those foods I would eat every day if I could. One really important ingredient that I use to make my ribs extra special is Red Pepper Jelly. I brush it on when they’re on the smoker. I also make sure I get some good quality meat. I like to run right down the road to Cowart’s Meat Market. They always hook me up! Smoking ribs takes a few hours, but letting them slow cook makes sure you have the best flavor possible. This is an easy recipe that will be sure to impress all your buddies.</p>

