		<h2>Grilled Spaghetti</h2>
		<p>I like a quick and easy meal just as much as the next person, but there&rsquo;s just something about adding your own flavor to a traditional dish that makes a little extra effort totally worthwhile.</p>
<p>And to tell y&rsquo;all the truth, I try and add that extra bit of flavor every time I cook something.</p>
<p>In this case, when I make a traditional favorite &ndash; spaghetti - I like to throw the ground beef on to my grill with some wood chips mixed in my charcoal. This gives me a very distinct and really great smoky flavor with my beef.</p>
<p>Add that awesome flavor in with some great sauce, some excellent mushrooms, and a piece of garlic bread on the side, and you&rsquo;ve got an easy dish that will get the attention of a ton of folks.</p>
<img src="images/recipes/grilled-spaghetti/image001.png" alt="image001">
<p>Meal 1: Grilled Spaghetti</p>
<p>Ingredients:</p>
<ol>
<li>5 pounds of hamburger meat</li>
<li>steak seasoning (sprinkle)</li>
<li>wood chips of your choice</li>
<li>(2) containers of spaghetti sauce</li>
<li>mushrooms</li>
<li>white wine (Moscato&mdash;sweet wine)</li>
<li>spaghetti noodles</li>
<li>olive oil</li>
<li>oregano</li>
<li>minced garlic</li>
</ol>
<p>Prep Time: 15 minutes</p>
<p>Cook Time: 1 hour</p>
<img src="images/recipes/grilled-spaghetti/image002.png" alt="image002">
<p>Add your charcoal and wood chips of choice, and light your grill. The wood chips will give your meat a bit of a smoky flavor.</p>
<img src="images/recipes/grilled-spaghetti/image003.png" alt="image003">
<p>Create 6-7 patties (differs for amount of spaghetti made). You can do this by hand or with a patty maker. Place in a tray. (Grilling kills most of the grease)</p>
<img src="images/recipes/grilled-spaghetti/image004.png" alt="image004">
<p>Season patties with your choice of grill seasoning. I use McCormick&rsquo;s Montreal Seasoning.</p>
<img src="images/recipes/grilled-spaghetti/image006.png" alt="image006">
<img src="images/recipes/grilled-spaghetti/image005.png" alt="image005">
<p>Then, throw the patties onto the grill, and shut the grill door. Let cook for 6-7 minutes, or until fully done (flip halfway into cook time).</p>
<img src="images/recipes/grilled-spaghetti/image007.png" alt="image007">
<p>Wash your mushrooms, place into a strainer to let water drain.</p>
<p> Dump your sauce into a pan, and turn on a medium-high heat.</p>
<img src="images/recipes/grilled-spaghetti/image008.png" alt="image008">
<p>Cut hamburger patties into chunks, and dump into sauce.</p>
<img src="images/recipes/grilled-spaghetti/image009.png" alt="image009">
<p>Add a fair amount of minced garlic into your sauce (for our portion, I added around 16 oz), and stir all together.</p>
<img src="images/recipes/grilled-spaghetti/image010.png" alt="image010">
<p>Cover sauce with lid, and turn heat down to Medium.</p>
<img src="images/recipes/grilled-spaghetti/image011.png" alt="image011">
<p>Pour whole mushrooms into a separate pan, and add your Moscato all around. I used around 5 ounces.</p>
<img src="images/recipes/grilled-spaghetti/image012.png" alt="image012">
<p>Add approximately 2-3 ounces of minced garlic, a pinch of seasoned salt, and a pinch of oregano to the mushrooms. Cover with lid, and turn on medium heat.</p>
<p>Let the sauce and mushrooms simmer for around 30 minutes with medium heat.</p>
<img src="images/recipes/grilled-spaghetti/image013.png" alt="image013">
<p>Fill a pot with 3 quarts of water, and add around an ounce of olive oil. Cover water with lid and bring to a boil.</p>
<img src="images/recipes/grilled-spaghetti/image014.png" alt="image014">
<p>Grab a handful of spaghetti noodles, break them in half, and add to boiling water. Stir in water, and add lid back to top of pot. Let noodles soften for around 10 minutes.</p>
<img src="images/recipes/grilled-spaghetti/image015.png" alt="image015">
<p>Drain liquids from mushrooms, and set to the side.</p>
<img src="images/recipes/grilled-spaghetti/image016.png" alt="image016">
<p>Dump Spaghetti into strainer, draining liquids, and one half stick of butter.</p>
<p>Plate the spaghetti, add your meat sauce and mushrooms on top of the pasta, and provide your guests with a breadstick on the side.</p>


