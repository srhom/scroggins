		<h2>Shrimp Stuffed Jalapenos</h2>
<img src="images/recipes/stuffed-jalapenos/image001.png" alt="image001" >
<p>I posted this picture on my Facebook page and had a ton of folks ask what I was cooking. The answer: Shrimp Stuffed Jalape&ntilde;os. &nbsp;This is a great appetizer that is easy to prepare. In fact, when weather rained-out our trip to the lake over Memorial Day Weekend a few weeks ago, all of the sudden I had a house full of hungry family and friends, and this is what I whipped up for them. Now they&rsquo;re all huge fans of Shrimp Stuffed Jalapenos. Cook &lsquo;em once for your friends, and I promise they&rsquo;ll request it again and again!</p>
<p><strong>Stuffed Jalapeno Shrimp Bacon Wraps</strong></p>
<p>Ingredients: 1lb jalapenos, 4-6oz cream cheese, 1-2lb shrimp, 1lb bacon, Seasoning of choice.</p>

<p>Tools needed: grill, grill Stone, cutting board, strainer,</p>

<p>Prep Time: 15 minutes</p>
<p>Cook Time: 15 minutes</p>
<img src="images/recipes/stuffed-jalapenos/image002.png" alt="image002" >

<p>Light your grill, and place a grilling stone on the lit coals to divert the heat and avoid scorching the bacon. Cut the stem ends off, and split the jalape&ntilde;os in half long ways.</p>
<img src="images/recipes/stuffed-jalapenos/image003.png" alt="image003" >
<img src="images/recipes/stuffed-jalapenos/image004.png" alt="image004" >

<p>Use your knife to cut out the vein through the middle of the jalape&ntilde;o with all of the seeds. If you do not remove your seeds your peppers will be very spicy &ndash; in fact, probably too hot for most people&rsquo;s liking.</p>
<img src="images/recipes/stuffed-jalapenos/image005.png" alt="image005" >

<p>After removing the seeds, place your hollowed out jalape&ntilde;os into your strainer and rinse them thoroughly.</p>
<img src="images/recipes/stuffed-jalapenos/image006.png" alt="image006" >

<p>Fill the hollowed out peppers with cream cheese.</p>
<img src="images/recipes/stuffed-jalapenos/image007.png" alt="image007" >

<p>Remove the tail from your shrimp, and place a single piece on top of your jalape&ntilde;o</p>
<img src="images/recipes/stuffed-jalapenos/image008.png" alt="image008" >
<img src="images/recipes/stuffed-jalapenos/image009.png" alt="image009" >

<p>Wrap your jalape&ntilde;o with half a slice of bacon and set into a baking dish. Repeat for your entire stock.</p>
<img src="images/recipes/stuffed-jalapenos/image010.png" alt="image010" >

<p>Once all the jalapenos are wrapped, sprinkle a seasoning of choice over them. I like to use Old Bay Blackened Seasoning. Place your jalape&ntilde;os onto the grill for around 15 minutes, turning them half way through.</p>
<img src="images/recipes/stuffed-jalapenos/image011.png" alt="image011" >

<p>Once your jalape&ntilde;os have cooked for 15 minutes, take them off and let cool for around 4-5 minutes before serving.</p>
<img src="images/recipes/stuffed-jalapenos/image012.png" alt="image012" >
<p>Once cooled, place on a plate, and serve. &nbsp;Now you have an easy appetizer or snack that all your buddies are going to be asking for each time they come for a visit.</p>

