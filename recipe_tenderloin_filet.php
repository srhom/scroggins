<p>This is all started one night when a bunch of folks were coming over to my house to watch a UFC fight on TV. I wanted to grill ribeyes for them, but the butcher ran out, so I kinda dared myself to grill the tenderloins he had for sale instead – and it’s turned out to be one of the best meats I’ve ever grilled.</p>

<p>Prep time: 40 minutes<br />
Pre-hat grill to 300 with 3 chunks of cherry wood</p>

<p>Indgredients:<br /> 
1 whole tenderloin 1 bottle of Lawry’s Teriyaki 1 injector McCormick’s grill mates Montreal steak seasoning Cherry wood</p>

<p>Directions:<br /> 
1) clean all excess fat from tenderloin 2) inject with teriyaki and let sit for 30-40 minutes 3) place on grill for 10-12 minutes 4) flip and grill for another 10-12 minutes 5) cut 1 ½ thickness and finish grilling like a steak to your likeness</p>

