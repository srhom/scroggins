<p>Prep time 15 minutes<br />
Cook time 15-30 minutes depending on desired temperature<br />
Servings 4</p>

<p>Indgredients:<br /> 
(4) 1 1/2 inch thick Montreal Steak Seasoning<br /> 
Black Pepper<br /> 
Lawry's Teriyaki Marinade</p>

<p>Directions:<br />
Preheat grill to 600 F Optional-4 pieces of Mesquite wood to your grill for smoke flavor. Marinate steaks in a zip lock bag with the Teriyaki marinade for 45 minutes. Season both sides of steak with seasonings and pepper. Place on grill for 2 minutes each side at 600 F then reduce heat to 350 until you reach your desired temperature.</p>

