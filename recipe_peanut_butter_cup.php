		<h2>Peanut Butter Cup Pie</h2>
<img src="images/recipes/peanut-butter-cup/image001.png" alt="image001">		
<p>When you&rsquo;re on the road as much as I am you have to get inventive with your desserts! This off-season I worked out a lot and lost a good bit of weight, but every once in a while I still like to enjoy a nice after dinner treat. This is a quick and easy one I make on the road, that doesn&rsquo;t take much to make and ALWAYS impresses the people I&rsquo;m hanging around with. The ingredients are simple to get, it&rsquo;s cheap to make, and I think you&rsquo;ll like it a lot.</p>

<p>Ingredients: 10 regular sized peanut butter cups, 2-8oz containers of Cool Whip, 1 pre-made graham cracker piecrust (optional: Cocoa dusted almonds, olive or canola oil)</p>

<p>Tools: Spoon or mixer, saucepan, large mixing bowl</p>
<p>Prep time: 10 minutes</p>
<p>Cool time: 2 hours</p>
<img src="images/recipes/peanut-butter-cup/image002.png" alt="image002">
<p>Turn on medium heat with a small saucepan. You can add a teaspoon of your desired oil to help with sticking if necessary.</p>
<img src="images/recipes/peanut-butter-cup/image004.png" alt="image004">
<img src="images/recipes/peanut-butter-cup/image005.png" alt="image005">
<img src="images/recipes/peanut-butter-cup/image003.png" alt="image003">

<p>Dump 10 Reese's cups in and stir while it melts. It is really important that you constantly stir so the peanut butter cups do not burn.</p>
<img src="images/recipes/peanut-butter-cup/image007.png" alt="image007">
<img src="images/recipes/peanut-butter-cup/image008.png" alt="image008">
<img src="images/recipes/peanut-butter-cup/image006.png" alt="image006">

<p>Dump the cool whip and melted peanut butter cups into a bowl and continue stirring to not allow it to harden. It needs to be thoroughly mixed before you finish mixing.</p>
<img src="images/recipes/peanut-butter-cup/image010.png" alt="image010">
<img src="images/recipes/peanut-butter-cup/image009.png" alt="image009">

<p>When thoroughly mixed dump into your graham cracker pie crust.</p>
<img src="images/recipes/peanut-butter-cup/image011.png" alt="image011">
<p>If desired, sprinkle some cocoa dusted almonds on top of the pie.</p>
<img src="images/recipes/peanut-butter-cup/image013.png" alt="image013">
<img src="images/recipes/peanut-butter-cup/image012.png" alt="image012">

<p>Place in freezer to set for 2 hours, and then serve.</p>

