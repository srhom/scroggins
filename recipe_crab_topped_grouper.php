		<h2>Crab Topped Grouper</h2>

<p>There are so many good things about living in Florida, but one of the best things is the availability of fresh seafood! There’s nothing better than being able to run down the road to Kyle’s Seafood and grab whatever seafood I’m hankering for. On this particular day I went and grabbed some fresh Grouper filets (you can find these at your local supermarket!), and then hopped down to my local supermarket for some Crab stuffing (just about every supermarket with seafood should have some). Grilling Grouper filets and topping them with Crab is one of my absolute favorite seafood dishes to make, it will impress your dinner guests, and it’s so easy to do!</p>

<img alt="image" src="images/recipes/grouper/Image_001.jpg" >

<img alt="image" src="images/recipes/grouper/Image_002.jpg" >

<p>Crabbed Topped Grouper</p>

<p>Ingredients: Grouper Filets, Crab Stuffing (3-­‐4 ounces per filet), Seafood seasoning of choice.</p>

<p>Prep Time:10 minutes Cook Time: 20 minutes</p>

<img alt="image" src="images/recipes/grouper/Image_003.jpg" >

<p>Light your grill, and set a pizza stone on your grate. Then set another grate on top of the pizza stone. I then spray the grate with cooking spray (to keep the filets from sticking to the grill), and let it heat to 325 degrees.</p>

<p>This lets your food cook without a direct flame scorching it.</p>

<img alt="image" src="images/recipes/grouper/Image_004.jpg" >

<p>Set your grouper on the grill and sprinkle your seasoning of choice. I use Seafood Magic by Chef Paul Prudhomme. Let each side of your Grouper cook for 9-­‐10 minutes.</p>

<img alt="image" src="images/recipes/grouper/Image_005.jpg" >

<img alt="image" src="images/recipes/grouper/Image_006.jpg" >

<p>Flip your grouper over, and sprinkle with your seasoning.</p>

<img alt="image" src="images/recipes/grouper/Image_007.jpg" >

<img alt="image" src="images/recipes/grouper/Image_008.jpg" >

<p>Set your crab stuffing on top of the grouper and let cook for another 10-­‐ 12 minutes. The Crab stuffing will spread across the meat and get a good “browned” look to it.</p>

<img alt="image" src="images/recipes/grouper/Image_009.jpg" >

<p>When done, remove your Crab Topped Grouper from the grill.</p>

<img alt="image" src="images/recipes/grouper/Image_010.jpg" >

<p>I serve my Crab Topped Grouper on a bed of rice, and garnish my plate with a few fresh Jalapeños.</p>
