            <h2>Terry Scroggins</h2>
            <p>Scroggins’ famous “It’s All Good” expression serves as a tremendous indicator of his daily approach to life. Terry learned humility, gratefulness and mechanical skill in a paint booth. For 16 years prior to becoming one of the most accomplished bass pros on the planet, Scroggins made a living driving a tow truck and painting cars in the family auto body business.</p>
            <p>Speaking of home and hand labor, it was an outboard mechanic in Scroggins’ hometown who deeply admired Terry’s dominance of area tournaments that told a group of unfounded braggadocios local anglers -- “Y’all better watch out for Scroggins, he’s The Big Show.” The nickname “Big Show” soon stuck.</p>
            <p>Scroggins quickly proved the supportive outboard mechanic true by reaching the $1,000,000 in BASS winnings faster than just about anybody in the history of the sport. Once perceived as only being able to win in his home state of Florida where he earned his nickname -- Scroggins has proven he can succeed anywhere in America. In early 2008, he missed eclipsing the BASS single-day catch record by just ounces in south Texas when he caught a 5-bass limit at Lake Falcon that weighed 44 pounds, 4 ounces.</p>
            <p>Scroggins has now qualified for 9 Bassmaster Classics; winning 5 B.A.S.S. tournaments along the way, and notching more than three dozen Top 10 finishes in what is still a relatively short career.</p>
            <p>His “how can I help you?” attitude, on the water success and contagious smile make him a favorite of media folks on a weekly basis. He’s quick to show up for any media outings his sponsors host, knowing the potential such events have to help the brands he proudly represents.</p>
            <p>And once on site, not only will he fish hard, work hard and smile pretty – he’ll also make his way to the kitchen or outdoor grill to fix dinner for everybody. Scroggins is famous among fellow pros and fishing industry media for his ribeyes and pepper jelly ribs.</p>
            <p>Food, fun, fishing success and hard work for his sponsors – all treasured traits of Terry “The Big Show” Scroggins.</p>
