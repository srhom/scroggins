               <h2>Grill Tips</h2>
               <div id="accordion">
                  <h3>What do you grill when you want to impress someone?</h3>
                  <div>
                     <p>When I want to impress someone, I like to cook a good-sized tenderloin, and I’ll inject it with Lawry’s Teriyaki sauce for flavor. I like it because it’s a massive piece of meat, and when you inject it, it stays really juicy and soft. When you are cooking for 8-10 people you cook the tenderloin as a whole, and then slice it up to cook to different temperatures as desired.</p>
                  </div>
                  <h3>What's a grilling mistake that everyone makes?</h3>
                  <div>
                     <p>The most common mistake people make when grilling is not keeping their temperature under control. A lot of times on gas grills folks will put their meat over the fire, causing the fats to run off and make the flames higher/hotter. Everything just tastes better when cooked a bit slower from a lower temperature.</p>
                  </div>
                  <h3>What is your grilling go-to or specialty dish?</h3>
                  <div>
                     <p>I like a nice inch and a half thick Rib eye as my go-to. I can throw a few of them into a Ziploc bag with some Lawry’s Teriyaki for half an hour and put them on the grill. It is super simple but is always guaranteed to be a hit.</p>
                  </div>
                  <h3>Dry rub or wet rub on ribs? Why?</h3>
                  <div>
                     <p>On ribs I prefer a wet rub, mainly because I like to rub Red Pepper Jelly. I believe wet rubs give you more flavor and you can add it at the end of your smoking procedure. I think it really gives you the x-factor to your ribs.</p>
                  </div>
                  <h3>Do you have a signature marinade and what is your marinade method?</h3>
                  <div>
                     <p>I wouldn’t classify it as a signature marinade, but Lawry’s Teriyaki marinade is a staple on my steaks, chicken, and most everything else I can throw on the grill. It won’t allow the meat to dry out, gives it a nice, sweet taste and it because it is easy to find as I travel around the country fishing tournaments.</p>
                  </div>
